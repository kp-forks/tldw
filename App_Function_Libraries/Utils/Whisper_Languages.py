def get_whisper_language_list():
    """Returns a list of tuples (code, name) sorted by language name for use in Gradio dropdowns"""
    # Sort by language name, but keep auto-detect at the top
    sorted_langs = sorted([(code, name) for code, name in WHISPER_LANGUAGES.items() if code != "auto"], 
                         key=lambda x: x[1])
    # Add auto-detect at the beginning
    return [("auto", "Auto-detect")] + sorted_langs

def get_language_names():
    """Returns just the language names for display"""
    return [name for _, name in get_whisper_language_list()]

def get_language_codes():
    """Returns just the language codes"""
    return [code for code, _ in get_whisper_language_list()]

def get_language_code(language_name):
    """Get language code from language name"""
    for code, name in WHISPER_LANGUAGES.items():
        if name == language_name:
            return code
    return "auto"  # Default to auto-detect if not found

def get_language_name(language_code):
    """Get language name from language code"""
    return WHISPER_LANGUAGES.get(language_code, "Auto-detect")